void main() {
  int diaDaSemana = 1;
  switch (diaDaSemana) {
    case 1:
     print("segunda-feira");
      break;
    case 2:
      print("terça-feira");
      break;
    case 3:
      print("quarta-feira");
      break;
    case 4:
      print("quinta-feira");
      break;
    case 5:
      print("sexta-feira");
      break;
    case 6:
      print("sábado");
      break;
    case 7:
      print("domingo");
      break;
    default:
      print("Dia inválido");
  }
}